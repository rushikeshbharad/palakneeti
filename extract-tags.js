function main() {
  console.log("Extracting tags from local ARTICLES…");

  const freq = new Map();

  for (const obj of ARTICLES) {
    const articleData = Object.values(obj)[0];
    const { title, text } = extractTextFromArticle(articleData);

    // Process content text
    const tokens = filterStopwords(extractDevanagariTokens(text));

    // Process title text and give it more weight
    const titleTokens = filterStopwords(extractDevanagariTokens(title));
    titleTokens.forEach(t => {
      freq.set(t, (freq.get(t) || 0) + 25);
    });

    // unigrams
    tokens.forEach(t => {
      freq.set(t, (freq.get(t) || 0) + 1);
    });

    if (GENERATE_NGRAMS) {
      const bigrams = generateNgrams(tokens, 2);
      const trigrams = generateNgrams(tokens, 3);
      for (const ngram of [...bigrams, ...trigrams]) {
        freq.set(ngram, (freq.get(ngram) || 0) + 1);
      }
    }
  }

  const arr = Array.from(freq.entries())
    .filter(([token, count]) => count >= MIN_FREQ_TO_INCLUDE)
    .sort((a, b) => b[1] - a[1]);

  const top = arr.slice(0, OUTPUT_TOP_N);

  fs.writeFileSync(
    "tags.txt",
    top.map(([t]) => t).join("\n"),
    { encoding: "utf8" }
  );

  const csvWriter = createObjectCsvWriter({
    path: "tags.csv",
    header: [
      { id: "tag", title: "tag" },
      { id: "count", title: "count" },
    ],
    encoding: "utf8",
  });

  csvWriter
    .writeRecords(top.map(([t, c]) => ({ tag: t, count: c })))
    .then(() =>
      console.log(
        `✅ Done! Wrote ${top.length} tags → tags.txt, tags.csv`
      )
    );
}

main();
